def load_posted():
    if POSTED_FILE.exists():
        return {
            post_id.strip()
            for post_id in POSTED_FILE.read_text(encoding="utf-8").splitlines()
            if post_id.strip()
        }
    return set()


def save_posted(post_id, posted_set):
    posted_set.add(post_id)
    # Keep the state file deterministic and make a successful send durable
    # before the next scheduled check.
    POSTED_FILE.write_text("\n".join(sorted(posted_set)) + "\n", encoding="utf-8")


def send_telegram(text):
    api_url = f"https://api.telegram.org/bot{TELEGRAM_TOKEN}/sendMessage"
    payload = json.dumps({
        "chat_id": TELEGRAM_CHAT,
        "text": text,
    }).encode("utf-8")
    req = urllib.request.Request(
        api_url, data=payload,
        headers={"Content-Type": "application/json"}
    )
    try:
        with urllib.request.urlopen(req, timeout=15) as r:
            resp = json.loads(r.read())
        return resp.get("ok", False)
    except Exception as e:
        log(f"Ошибка отправки: {e}")
        return False


def log(msg):
    logfile = Path(__file__).parent / "tg_scheduler.log"
    line = f"[{datetime.now().strftime('%d.%m %H:%M')}] {msg}\n"
    with open(logfile, "a", encoding="utf-8") as f:
        f.write(line)


def check_and_post():
    now = datetime.now(TIMEZONE)
    today = now.date()
    posted = load_posted()

    for (post_date, post_time, post_id, text) in POSTS:
        if post_id in posted:
            continue
        p_date = date.fromisoformat(post_date)
        p_time = datetime.strptime(post_time, "%H:%M").time()

        # A post is relevant only on its planned calendar day.  This prevents
        # an interrupted scheduler from publishing a backlog of stale posts
        # when it is started again days later.
        if p_date < today:
            save_posted(post_id, posted)
            log(f"Пропущен устаревший пост: {post_id} ({post_date} {post_time})")
            continue

        if today == p_date and now.time() >= p_time:
            log(f"Постю: {post_id}")
            if send_telegram(text):
                save_posted(post_id, posted)
                log(f"Отправлено: {post_id}")
                time.sleep(5)


def main():
    log("=== Scheduler запущен ===")
    if not TELEGRAM_TOKEN:
        log("Ошибка: не задан секрет TELEGRAM_TOKEN")
        return
    try:
        # GitHub Actions invokes this script every 30 minutes.  It must make
        # one check and exit so that the workflow can commit its state.
        check_and_post()
    except Exception as e:
        log(f"Ошибка: {e}")


if __name__ == "__main__":
    main()
